/*******************************************************************************
* Function Name  : EXTI9_5_IRQHandler
* Description    : This function handles External lines 9 to 5 interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void EXTI9_5_IRQHandler(void)
{	
  if(EXTI_GetITStatus(EXTI_Line6)!= RESET)  //左脚
  {  
     //delay_nus(50);
    if(L_DETECTION==Bit_SET) //左脚抬起 
      Pedal &= (~L_DETECTION_FLAG);
    else            //左脚踩下      
    {
//      test++;
      Pedal |= L_DETECTION_FLAG;
    }
     
    EXTI_ClearFlag(EXTI_Line6);
  }
  if(EXTI_GetITStatus(EXTI_Line7)!= RESET) //右脚
  {    
     delay_nus(40);
    if(R_DETECTION==Bit_SET) //右脚抬起
     Pedal &= (~R_DETECTION_FLAG);
    else            //右脚踩下 
      Pedal |= R_DETECTION_FLAG;
    EXTI_ClearFlag(EXTI_Line7);  
  }  
  
   if(EXTI_GetITStatus(EXTI_Line8)!= RESET) //右脚
  {  
     delay_nus(40);
     if(L_DETECTION==Bit_SET) //左脚抬起 
     { 
       
       if(READ_GAS)
         GAS_0;
       else
         GAS_1;
     } 
     EXTI_ClearFlag(EXTI_Line8);  
  }  
  
  if((memory[TEST_ACTION])&&(Pedal&0x0f))  //自检标识
    memory[TEST_ACTION]=0;
  
  if((Pedal==LRE_L1R0)&& HotBlowerContinue_falg)
   {
     PRESSURE_1; //下压
     Pedal=LR_L1R0;
     Pedal_old=Pedal;
     HotBlowerContinue_falg=0;
   }
   else if(Pedal==LRE_L0R0)
   {
     HotBlowerContinue_falg=1;
   }
  
//  if(EXTI_GetITStatus(EXTI_Line8)!= RESET)
//  {    
//    EXTI_ClearFlag(EXTI_Line8);
//    EXTI_ClearITPendingBit(EXTI_Line8);   
//  }
//  if(EXTI_GetITStatus(EXTI_Line9)!= RESET)
//  {    
//    EXTI_ClearFlag(EXTI_Line9);
//    EXTI_ClearITPendingBit(EXTI_Line9);   
//  }
  
}

