/*******************************************************************************
* Function Name  : SysTickHandler
* Description    : This function handles SysTick Handler.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void SysTickHandler(void)
{
  if(uart1_rxflag)
  {
    uart1_time ++;
    if(uart1_time >10)
    {
      uart1_rxflag=0;
      if(uart1_rxptr>4)
      {
        uart1_time=0;
        Modbus_Rxflag=1;
      }
      else
      {
        uart1_rxptr=0;
        uart1_time=0;
      }  
    } 
  }
  
  if(Delay_flag)
    Delay_Cnt++;
  
  Sec_Cnt++;
  if(Sec_Cnt % 500  == 0)
  {
    if(memory[PIDSET_ADR]||memory[TEMPCONTROL_ADR])
    {
      KV_value=PV_value-SV_value;
      if(KV_value<=2 && KV_value>=-2)
        memory[READ_CURRENTTEMP_ADR]=(unsigned short)(SV_value*10);
      else if(KV_value<=5 && KV_value>=-5)
        memory[READ_CURRENTTEMP_ADR]=(unsigned short)((PV_value+KV_value/2)*10);
      else
        memory[READ_CURRENTTEMP_ADR]=(unsigned short)(PV_value*10);
      
      
      if(memory[TEMPCONTROL_ADR])  //温控打开
      {
        if((Fuzzy_flag==0)&&(KV_value<=2) && (KV_value>=-2)) //模糊未打开 并且温控偏差在+-2度
        {
          Fuzzy_cnt++;
          if(Fuzzy_cnt>10000)  //稳定10S
          {
            Fuzzy_flag=1; //关闭模糊
          }
        }
        else
        {
          Fuzzy_cnt=0;
        }
      }
     
    }
    else
      memory[READ_CURRENTTEMP_ADR]=(unsigned short)(PV_value*10); 
  }
  if(Sec_Cnt >= 1000 )//1s	 如果要想1000对应1s 那么中间不能有关中断的行为发生
  {
    Sec_Cnt = 0; 
    if(Ds18b20_readflag==0)  //获取室温
    {
      Ds18b20_readcnt++;
      if(Ds18b20_readcnt>=60) //1分钟获取一下室温
      {
        Ds18b20_readcnt=0;
        Ds18b20_readflag=1;
      }
    }
  }
  if(memory[PIDSET_ADR] && PidSet_flag) //PID自整定开始计时
   {
      pid_self_time_cnt++;
   }
  
  
}

