/*******************************************************************************
* Function Name  : USART1_IRQHandler
* Description    : This function handles USART1 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART1_IRQHandler(void)
{
  if(USART1->SR & (1<<6))
  {
    if(uart1_txptr < uart1_txall)
    {
      uart1_txptr++;
      USART1->DR = uart1_txbuf[uart1_txptr];
      uart1_txbuf[uart1_txptr] = 0;
    }
    else
    {
      uart1_txptr = 0;
      uart1_txall = 0;
      USART1->SR &= ~(1<<6);
    }
    
  }
  if(USART1->SR & (1<<5))
  {
    uart1_rxflag=1;
    uart1_rxbuf[uart1_rxptr] = USART1->DR;
    if(uart1_rxptr>200)
      uart1_rxptr=0;
    else 
       uart1_rxptr ++;
    uart1_time = 0;
  }
}

